import os
import sys

# –¢–∞–±–ª–∏—Ü–∞ TW (Table of Words) - –∑–∞—Ä–µ–∑–µ—Ä–≤–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Å–ª–æ–≤–∞ –Ω–∞—à–µ–≥–æ —è–∑—ã–∫–∞
# –ö–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ –∏–º–µ–µ—Ç —Å–≤–æ–π –∏–Ω–¥–µ–∫—Å (–Ω–æ–º–µ—Ä). –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –ø–æ–¥ –Ω–æ–º–µ—Ä–æ–º 0.
TW = ["", "read", "write", "if", "then", "else", "for", "to", "while", "do", "true", "false", "or", "and", "not", "as"]

# –¢–∞–±–ª–∏—Ü–∞ TL (Table of Limiters) - –∑–Ω–∞–∫–∏ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è –∏ –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏–µ —Å–∏–º–≤–æ–ª—ã
# –ù–∞–ø—Ä–∏–º–µ—Ä, –∏–Ω–¥–µ–∫—Å 1 - —ç—Ç–æ '{', –∏–Ω–¥–µ–∫—Å 13 - —ç—Ç–æ '+'
TL = ["", "{", "}", "%", "!", "$", ",", ";", "[", "]", ":", "(", ")", "+", "-", "*", "/", "=", "<>", ">", "<", "<=",
      ">=", "/*"]

# –°—é–¥–∞ –º—ã –±—É–¥–µ–º –∑–∞–ø–∏—Å—ã–≤–∞—Ç—å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ, –∫–æ—Ç–æ—Ä—ã–µ –ø—Ä–∏–¥—É–º–∞–µ—Ç –ø—Ä–æ–≥—Ä–∞–º–º–∏—Å—Ç (–Ω–∞–ø—Ä–∏–º–µ—Ä, x, sum, n)
mTI = []
# –°—é–¥–∞ –±—É–¥–µ–º –∑–∞–ø–∏—Å—ã–≤–∞—Ç—å –≤—Å–µ —á–∏—Å–ª–∞, –∫–æ—Ç–æ—Ä—ã–µ –≤—Å—Ç—Ä–µ—Ç–∏–º –≤ –∫–æ–¥–µ (10, 3.14, 101B)
mTN = []


class FullAnalyzer:
    def __init__(self, content):
        self.content = content  # –•—Ä–∞–Ω–∏–º –∏—Å—Ö–æ–¥–Ω—ã–π —Ç–µ–∫—Å—Ç –ø—Ä–æ–≥—Ä–∞–º–º—ã
        self.pos = 0  # –¢–µ–∫—É—â–∞—è –ø–æ–∑–∏—Ü–∏—è (–Ω–æ–º–µ—Ä –±—É–∫–≤—ã), –∫–æ—Ç–æ—Ä—É—é –º—ã —á–∏—Ç–∞–µ–º
        self.ch = ''  # –¢–µ–∫—É—â–∏–π —Å–∏–º–≤–æ–ª (–±—É–∫–≤–∞), –∫–æ—Ç–æ—Ä—ã–π –º—ã —Å–µ–π—á–∞—Å –∏–∑—É—á–∞–µ–º
        self.stack = ""  # "–ú–µ—à–æ—á–µ–∫" –¥–ª—è –Ω–∞–∫–æ–ø–ª–µ–Ω–∏—è –±—É–∫–≤ (—Å–æ–±–∏—Ä–∞–µ–º –∏–∑ –Ω–∏—Ö —Å–ª–æ–≤–∞)
        self.tokens = []  # –°–ø–∏—Å–æ–∫ –∫–æ–¥–æ–≤ (—Ç–∏–ø, –∏–Ω–¥–µ–∫—Å) –¥–ª—è —Ä–∞–±–æ—Ç—ã –ø–∞—Ä—Å–µ—Ä–∞
        self.t_pos = 0  # –ù–æ–º–µ—Ä —Ç–µ–∫—É—â–µ–≥–æ —Ç–æ–∫–µ–Ω–∞ –¥–ª—è —Å–∏–Ω—Ç–∞–∫—Å–∏—á–µ—Å–∫–æ–≥–æ –∞–Ω–∞–ª–∏–∑–∞
        self.cur = (0, 0)  # –¢–µ–∫—É—â–∏–π —Ç–æ–∫–µ–Ω, –Ω–∞ –∫–æ—Ç–æ—Ä—ã–π —Å–º–æ—Ç—Ä–∏—Ç –ø–∞—Ä—Å–µ—Ä

    # –§—É–Ω–∫—Ü–∏—è 'gc' (Get Character) - –±–µ—Ä–µ—Ç —Å–ª–µ–¥—É—é—â—É—é –±—É–∫–≤—É –∏–∑ —Ç–µ–∫—Å—Ç–∞
    def gc(self):
        if self.pos < len(self.content):
            self.ch = self.content[self.pos]
            self.pos += 1
        else:
            self.ch = '\0'  # –ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –∫–æ–Ω—á–∏–ª—Å—è, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º "–ø—É—Å—Ç–æ–π —Å–∏–º–≤–æ–ª"

    # –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ –æ—à–∏–±–æ–∫ - —Å—Ä–∞–∑—É –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –ø—Ä–æ–≥—Ä–∞–º–º—É
    def error(self, msg):
        print(f"\nüõë –û–®–ò–ë–ö–ê: {msg}")
        sys.exit(1)

    # –§—É–Ω–∫—Ü–∏—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è —Ç–æ–∫–µ–Ω–∞ - –¥–µ–ª–∞–µ—Ç "–∫—Ä–∞—Å–∏–≤—ã–π" –∏ "—Ü–∏—Ñ—Ä–æ–≤–æ–π" –≤—ã–≤–æ–¥ –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ
    def add_token(self, t_type, t_index, value=None):
        # t_type: 1-–°–ª–æ–≤–æ, 2-–ó–Ω–∞–∫, 3-–ß–∏—Å–ª–æ, 4-–ò–º—è –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
        self.tokens.append((t_type, t_index))

        # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç–µ–∫—Å—Ç–æ–≤–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ —Ç–∏–ø–∞
        type_names = {1: "–°–õ–û–í–û (TW)", 2: "–ó–ù–ê–ö (TL)", 3: "–ß–ò–°–õ–û (mTN)", 4: "–ü–ï–†–ï–ú–ï–ù–ù–ê–Ø (mTI)"}
        t_name = type_names.get(t_type, "???")

        # –ù–∞—Ö–æ–¥–∏–º —Å–∞–º–æ –∑–Ω–∞—á–µ–Ω–∏–µ (—Å–ª–æ–≤–æ –∏–ª–∏ –∑–Ω–∞–∫) –∏–∑ —Ç–∞–±–ª–∏—Ü –ø–æ –∏–Ω–¥–µ–∫—Å—É
        if t_type == 1:
            display_val = TW[t_index]
        elif t_type == 2:
            display_val = TL[t_index]
        else:
            display_val = value  # –î–ª—è —á–∏—Å–µ–ª –∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –±–µ—Ä–µ–º —Å–∞–º–æ –∑–Ω–∞—á–µ–Ω–∏–µ

        # –í—ã–≤–æ–¥–∏–º –≤ –∫–æ–Ω—Å–æ–ª—å –≤–æ–æ–±—â–µ –≤—Å—ë: –¢–∏–ø, –ö–æ–¥—ã –∏ –ó–Ω–∞—á–µ–Ω–∏–µ
        print(f"[{t_name}] –¢–∏–ø: {t_type}, –ò–Ω–¥–µ–∫—Å: {t_index} | –ó–Ω–∞—á–µ–Ω–∏–µ: '{display_val}'")

    # === –õ–ï–ö–°–ò–ß–ï–°–ö–ò–ô –ê–ù–ê–õ–ò–ó–ê–¢–û–† (–°–ö–ê–ù–ï–†) ===
    # –ï–≥–æ —Ä–∞–±–æ—Ç–∞: –ø—Ä–µ–≤—Ä–∞—Ç–∏—Ç—å —Ç–µ–∫—Å—Ç –≤ —Å–ø–∏—Å–æ–∫ —Ç–æ–∫–µ–Ω–æ–≤
    def scan(self):
        state = "H"  # H - —ç—Ç–æ "–Ω–∞—á–∞–ª—å–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ" (–∂–¥—ë–º —Å–∏–º–≤–æ–ª)
        self.gc()
        while True:
            if state == "H":
                while self.ch.isspace(): self.gc()  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—Ä–æ–±–µ–ª—ã
                if self.ch == '\0': break  # –ï—Å–ª–∏ –≤—Å—ë –ø—Ä–æ—á–∏—Ç–∞–ª–∏ - –≤—ã—Ö–æ–¥–∏–º
                self.stack = ""  # –û—á–∏—â–∞–µ–º –º–µ—à–æ—á–µ–∫ –¥–ª—è –Ω–æ–≤–æ–≥–æ —Å–ª–æ–≤–∞

                if self.ch.isalpha():  # –ï—Å–ª–∏ —ç—Ç–æ –±—É–∫–≤–∞ - –Ω–∞—á–∏–Ω–∞–µ–º —Å–æ–±–∏—Ä–∞—Ç—å –ò–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä
                    self.stack += self.ch;
                    self.gc();
                    state = "I"
                elif self.ch.isdigit():  # –ï—Å–ª–∏ —ç—Ç–æ —Ü–∏—Ñ—Ä–∞ - –Ω–∞—á–∏–Ω–∞–µ–º —Å–æ–±–∏—Ä–∞—Ç—å –ß–∏—Å–ª–æ
                    self.stack += self.ch;
                    self.gc();
                    state = "N"
                elif self.ch == '/':  # –ï—Å–ª–∏ —Å–ª—ç—à - —ç—Ç–æ –º–æ–∂–µ—Ç –±—ã—Ç—å –¥–µ–ª–µ–Ω–∏–µ –∏–ª–∏ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π
                    self.gc();
                    state = "C1"
                elif self.ch == '<':  # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–Ω–∞–∫–æ–≤ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è
                    self.gc();
                    state = "M1"
                elif self.ch == '>':
                    self.gc();
                    state = "M2"
                elif self.ch in "{}().,;=+-*":  # –û–¥–∏–Ω–æ—á–Ω—ã–µ –∑–Ω–∞–∫–∏
                    if self.ch in TL:
                        self.add_token(2, TL.index(self.ch))
                    self.gc()
                else:
                    self.error(f"–ù–µ–ø–æ–Ω—è—Ç–Ω—ã–π —Å–∏–º–≤–æ–ª: {self.ch}")

            elif state == "I":  # –°–æ–±–∏—Ä–∞–µ–º —Å–ª–æ–≤–æ (–±—É–∫–≤—ã + —Ü–∏—Ñ—Ä—ã)
                while self.ch.isalnum():
                    self.stack += self.ch;
                    self.gc()
                if self.stack in TW:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ –∑–∞—Ä–µ–∑–µ—Ä–≤–∏—Ä–æ–≤–∞–Ω–æ –ª–∏ —ç—Ç–æ —Å–ª–æ–≤–æ
                    self.add_token(1, TW.index(self.stack))
                else:  # –ï—Å–ª–∏ –Ω–µ—Ç - —ç—Ç–æ –Ω–æ–≤–∞—è –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è
                    if self.stack not in mTI: mTI.append(self.stack)
                    self.add_token(4, mTI.index(self.stack) + 1, self.stack)
                state = "H"

            elif state == "N":  # –°–æ–±–∏—Ä–∞–µ–º —á–∏—Å–ª–æ (–ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç 101B, 77O, 10AH)
                while self.ch.isalnum() or self.ch == '.':
                    self.stack += self.ch;
                    self.gc()
                if self.stack not in mTN: mTN.append(self.stack)
                self.add_token(3, mTN.index(self.stack) + 1, self.stack)
                state = "H"

            elif state == "C1":  # –≠—Ç–æ –±—ã–ª —Å–ª—ç—à. –ï—Å–ª–∏ –∑–∞ –Ω–∏–º *, —Ç–æ —ç—Ç–æ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π
                if self.ch == '*':
                    self.gc(); state = "C2"
                else:
                    self.add_token(2, 16); state = "H"  # –ò–Ω–∞—á–µ —ç—Ç–æ –ø—Ä–æ—Å—Ç–æ –¥–µ–ª–µ–Ω–∏–µ /
            elif state == "C2":  # –í–Ω—É—Ç—Ä–∏ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è, –∂–¥–µ–º –∑–≤–µ–∑–¥–æ—á–∫—É –¥–ª—è –≤—ã—Ö–æ–¥–∞
                while self.ch != '*' and self.ch != '\0': self.gc()
                if self.ch == '\0': self.error("–ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π –Ω–µ –∑–∞–∫—Ä—ã—Ç!")
                self.gc();
                state = "C3"
            elif state == "C3":  # –ü–æ—Å–ª–µ –∑–≤–µ–∑–¥–æ—á–∫–∏ –∂–¥–µ–º —Å–ª—ç—à
                if self.ch == '/':
                    self.gc(); state = "H"
                else:
                    state = "C2"  # –ï—Å–ª–∏ –Ω–µ —Å–ª—ç—à, –∏–¥–µ–º –æ–±—Ä–∞—Ç–Ω–æ –≤ –∫–æ–º–º–µ–Ω—Ç—ã

            elif state == "M1":  # –†–∞–∑–±–æ—Ä <, <= –∏–ª–∏ <>
                if self.ch == '>':
                    self.gc(); self.add_token(2, 18)
                elif self.ch == '=':
                    self.gc(); self.add_token(2, 21)
                else:
                    self.add_token(2, 20)
                state = "H"
            elif state == "M2":  # –†–∞–∑–±–æ—Ä > –∏–ª–∏ >=
                if self.ch == '=':
                    self.gc(); self.add_token(2, 22)
                else:
                    self.add_token(2, 19)
                state = "H"

    # === –°–ò–ù–¢–ê–ö–°–ò–ß–ï–°–ö–ò–ô –ê–ù–ê–õ–ò–ó–ê–¢–û–† (–ü–ê–†–°–ï–†) ===
    # –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –ø—Ä–∞–≤–∏–ª—å–Ω–æ –ª–∏ —Å—Ç–æ—è—Ç —Ç–æ–∫–µ–Ω—ã (–ø–æ—Ä—è–¥–æ–∫ —Å–ª–æ–≤)
    def parse(self):
        self.t_pos = 0
        self.get_t()  # –ë–µ—Ä–µ–º —Å–∞–º—ã–π –ø–µ—Ä–≤—ã–π —Ç–æ–∫–µ–Ω
        self.block()  # –ù–∞—á–∏–Ω–∞–µ–º —Ä–∞–∑–±–æ—Ä —Å –ø—Ä–∞–≤–∏–ª–∞ –ë–ª–æ–∫
        print("\nüèÜ –°–ò–ù–¢–ê–ö–°–ò–° –í–ï–†–ù–´–ô: –ü—Ä–æ–≥—Ä–∞–º–º–∞ –ø–æ—Å—Ç—Ä–æ–µ–Ω–∞ –ø—Ä–∞–≤–∏–ª—å–Ω–æ.")

    # –í–∑—è—Ç—å —Å–ª–µ–¥—É—é—â–∏–π —Ç–æ–∫–µ–Ω –∏–∑ —Ç–µ—Ö, —á—Ç–æ –Ω–∞—à–µ–ª —Å–∫–∞–Ω–µ—Ä
    def get_t(self):
        if self.t_pos < len(self.tokens):
            self.cur = self.tokens[self.t_pos]
            self.t_pos += 1
        else:
            self.cur = (0, 0)  # –°–∏–≥–Ω–∞–ª –∫–æ–Ω—Ü–∞

    # –§—É–Ω–∫—Ü–∏—è "–°–æ–ø–æ—Å—Ç–∞–≤–ª–µ–Ω–∏–µ" - –ø—Ä–æ–≤–µ—Ä—è–µ—Ç, —Å–æ–≤–ø–∞–¥–∞–µ—Ç –ª–∏ —Ç–µ–∫—É—â–∏–π —Ç–æ–∫–µ–Ω —Å —Ç–µ–º, —á—Ç–æ –º—ã –∂–¥–µ–º
    def match(self, t, k):
        if self.cur[0] == t and (k == 0 or self.cur[1] == k):
            self.get_t()  # –ï—Å–ª–∏ —Å–æ–≤–ø–∞–ª–æ - –∏–¥–µ–º –¥–∞–ª—å—à–µ
        else:
            self.error(f"–°–∏–Ω—Ç–∞–∫—Å–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞! –ñ–¥–∞–ª —Ç–∏–ø {t} –∏–Ω–¥–µ–∫—Å {k}, –∞ –≤—Å—Ç—Ä–µ—Ç–∏–ª {self.cur}")

    # –ü—Ä–∞–≤–∏–ª–æ –ë–õ–û–ö: { –æ–ø–µ—Ä–∞—Ç–æ—Ä; –æ–ø–µ—Ä–∞—Ç–æ—Ä; }
    def block(self):
        self.match(2, 1)  # –ñ–¥–µ–º {
        while self.cur != (2, 2) and self.cur != (0, 0):
            self.statement()  # –†–∞–∑–±–∏—Ä–∞–µ–º –æ–ø–µ—Ä–∞—Ç–æ—Ä –≤–Ω—É—Ç—Ä–∏
            if self.cur == (2, 7): self.get_t()  # –ï—Å–ª–∏ –≤–∏–¥–∏–º ';', –ø—Ä–æ–ø—É—Å–∫–∞–µ–º –µ–≥–æ
        self.match(2, 2)  # –ñ–¥–µ–º }

    # –ü—Ä–∞–≤–∏–ª–æ –û–ü–ï–†–ê–¢–û–†: if, while, read, write –∏–ª–∏ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ
    def statement(self):
        t, k = self.cur
        if t == 4:  # –ï—Å–ª–∏ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π - –∑–Ω–∞—á–∏—Ç —ç—Ç–æ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–Ω–∏–µ
            self.get_t()
            self.match(1, 15)  # –û–±—è–∑–∞—Ç–µ–ª—å–Ω–æ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å —Å–ª–æ–≤–æ 'as'
            self.expr()  # –ê –ø–æ—Ç–æ–º –∫–∞–∫–æ–µ-—Ç–æ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ
        elif t == 1:  # –ö–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞
            if k == 1:  # read(x)
                self.get_t();
                self.match(2, 11);
                self.match(4, 0);
                self.match(2, 12)
            elif k == 2:  # write(–≤—ã—Ä–∞–∂–µ–Ω–∏–µ)
                self.get_t();
                self.match(2, 11);
                self.expr();
                self.match(2, 12)
            elif k == 3:  # if —É—Å–ª–æ–≤–∏–µ then –æ–ø–µ—Ä–∞—Ç–æ—Ä
                self.get_t();
                self.expr();
                self.match(1, 4);
                self.statement()
                if self.cur == (1, 5):  # –µ—Å–ª–∏ –µ—Å—Ç—å else
                    self.get_t();
                    self.statement()
            elif k == 8:  # while —É—Å–ª–æ–≤–∏–µ do –æ–ø–µ—Ä–∞—Ç–æ—Ä
                self.get_t();
                self.expr();
                self.match(1, 9);
                self.statement()
        elif self.cur == (2, 1):  # –ï—Å–ª–∏ –≤—Å—Ç—Ä–µ—Ç–∏–ª–∏ {, –∑–Ω–∞—á–∏—Ç —ç—Ç–æ –≤–ª–æ–∂–µ–Ω–Ω—ã–π –±–ª–æ–∫
            self.block()

    # –ü—Ä–∞–≤–∏–ª–æ –í–´–†–ê–ñ–ï–ù–ò–ï: —á–∏—Å–ª–æ –∏–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è, –≤–æ–∑–º–æ–∂–Ω–æ —Å–æ –∑–Ω–∞–∫–∞–º–∏ + - > –∏ —Ç.–¥.
    def expr(self):
        self.operand()  # –î–æ–ª–∂–Ω–æ –±—ã—Ç—å —á–∏—Å–ª–æ –∏–ª–∏ –∏–º—è
        while self.cur[0] == 2 and (13 <= self.cur[1] <= 22):  # –ü–æ–∫–∞ –µ—Å—Ç—å –∑–Ω–∞–∫–∏ –æ–ø–µ—Ä–∞—Ü–∏–π
            self.get_t()  # –°—ä–µ–¥–∞–µ–º –∑–Ω–∞–∫
            self.operand()  # –°–Ω–æ–≤–∞ –∂–¥–µ–º —á–∏—Å–ª–æ –∏–ª–∏ –∏–º—è

    # –ü—Ä–∞–≤–∏–ª–æ –û–ü–ï–†–ê–ù–î
    def operand(self):
        if self.cur[0] == 3 or self.cur[0] == 4:  # –õ–∏–±–æ —á–∏—Å–ª–æ, –ª–∏–±–æ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è
            self.get_t()
        else:
            self.error("–í –≤—ã—Ä–∞–∂–µ–Ω–∏–∏ –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç —á–∏—Å–ª–∞ –∏–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π!")


